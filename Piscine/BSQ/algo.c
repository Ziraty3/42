void	calculpos(int **matbn)
{
	int	x;
	int	y;
	
	while (matbn[y])
	{
		if (y != 0)
			while (matbn[y][x])
			{
				if(x != 0)
				{
					matbn[y][x] = matbn[y - 1][x] + matbn[y][x - 1] - matbn[y - 1][x - 1] + matbn[y][x];
					x++;
				}
			}
		y++;
	}
}
